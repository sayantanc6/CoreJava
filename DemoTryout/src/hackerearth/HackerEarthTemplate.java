package hackerearth;

import java.util.Scanner;

public class HackerEarthTemplate {

	public static void main(String[] args) {
		Scanner scanner = new Scanner(System.in);
		int t = scanner.nextInt();
		
		if (t > 0) {
			while (t > 0) {
				
				t--;
			}
			
		}else {
			System.exit(0); 
		}
	}

}
